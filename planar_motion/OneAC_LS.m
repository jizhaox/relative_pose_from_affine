function sols = OneAC_LS(data)
[C0,C1] = setup_elimination_template(data);
C1 = C0 \ C1;
RR = [-C1(end-7:end,:);eye(8)];
AM_ind = [1,2,3,6,4,5,7,8];
AM = RR(AM_ind,:);
[V,D] = eig(AM);
scale = sqrt(diag(D).' ./ ((V(3,:).*V(7,:))));
V = V .* (ones(size(V,1),1)*scale);
sols(1,:) = V(1,:);
sols(2,:) = V(3,:);
sols(3,:) = V(5,:);
sols(4,:) = V(7,:);
sols(5,:) = V(6,:) ./ (sols(3,:));
sols(6,:) = V(2,:) ./ (sols(1,:));

% Action =  x2*x4
% Quotient ring basis (V) = x1,x1*x6,x2,x2*x6,x3,x3*x5,x4,x4*x5,
% Available monomials (RR*V) = x1*x2*x4,x1*x2*x4*x6,x2^2*x4,x2*x3*x4,x2*x3*x4*x5,x2^2*x4*x6,x2*x4^2,x2*x4^2*x5,x1,x1*x6,x2,x2*x6,x3,x3*x5,x4,x4*x5,
